/* OPERADORES ARITMETICOS
Suma +
Resta -
Multiplicación *
División /
Modulo %
Exponente **
*/

let a = 5, b = 2;

let suma = a + b;
let resta = a - b;
let multiplicacion = a * b;
let division = a / b;
let modulo = a % b;
let exponente = a ** b;

console.log(suma);
console.log(resta);
console.log(multiplicacion);
console.log(division);
console.log(modulo);
console.log(exponente);


/*Operadores incremento, decremento
Incremento ++
Decremento --
*/

let x = 5;
let y = 5;
let w = 0;
let z = 0;

//INCREMENTO

//Pre-incremento
z = ++x;
console.log(x);
console.log(z);


//Post-incremento
w = y++;
console.log(y);
console.log(w);


//DECREMENTO

//Pre-decremento
z = --x;
console.log(x);
console.log(z);

//Post-decremento
w = y--;
console.log(y);
console.log(w);